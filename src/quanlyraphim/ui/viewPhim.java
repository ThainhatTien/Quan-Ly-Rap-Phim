    void find() {
        if (txtFind.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn Chưa nhập mã lịch chiếu");
        } else {
            LichChieu p = dao.selectById(txtFind.getText());
            if (p != null) {
                txtGioChieu.setText(p.getGioChieu());
                txtMaPhim.setText(p.getMaPhim());
                txtMaPhong.setText(p.getMaPhong());
                txtMaLichChieu.setText(p.getMaLichChieu());
                txtNgayChieu.setText(XDate.toString(p.getNgayChieu(), "dd/MM/yyyy"));
            }
        }
    }
