    void edit() {
        String malc = (String) tblViewPhim.getValueAt(this.row, 0);
        LichChieu lc = dao.selectById(malc);
        this.setForm(lc);
//        tabs.setSelectedIndex(0);
//        this.updateStatus();
    }

    void setForm(LichChieu lc) {
        txtMaLichChieu.setText(lc.getMaLichChieu());
        txtMaPhong.setText(lc.getMaPhong());
        txtGioChieu.setText(lc.getGioChieu());
        txtNgayChieu.setText(XDate.toString(lc.getNgayChieu(), "dd/MM/yyyy"));
        hd.setMaphong(lc.getMaPhong());
    }

    LichChieu getForm() {
        LichChieu lc = new LichChieu();
        lc.setMaLichChieu(txtMaLichChieu.getText());
        lc.setMaPhong(txtMaPhong.getText());
        lc.setGioChieu(txtGioChieu.getText());
        lc.setNgayChieu(XDate.toDate(txtNgayChieu.getText(), "dd/MM/yyyy"));
        return lc;
    }

