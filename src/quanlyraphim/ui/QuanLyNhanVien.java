    public boolean checkValidateForm() {
        if (txtMaNV.getText().isEmpty() || txtTenNV.getText().isEmpty() || txtMatKhau.getText().isEmpty()
                || txtDiaChi.getText().isEmpty() || txtNgaySinh.getText().isEmpty() || txtSoDienThoai.getText().isEmpty()) {
            MsgBox.alert(this, "Chưa nhập đủ thông tin");
            return false;
        }
        return true;
    }
    int ranNum = (int) (Math.random() * 1000000000);

    void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblNhanVien.getModel();
        model.setRowCount(0);
        try {
            List<NhanVien> list = dao.selectAll();
            for (NhanVien nv : list) {
                Object[] row = {
                    nv.getMaNV(),
                    nv.getTenNV(),
                    nv.getMatKhau() + ranNum,
                    nv.getNgaySinh(),
                    nv.getDiaChi(),
                    nv.getSoDT(),
                    nv.getGioiTinh(),
                    nv.getVaiTro()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }

    void insert() {
        NhanVien nv = this.getForm();
        try {
            if (checkValidateForm()) {
                dao.insert(nv); // thêm mới
                this.fillTable(); // đỗ lại bảng
                this.clearForm(); // xóa trắng form
                MsgBox.alert(this, "Thêm mới thành công!");
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Thêm mới thất bại!\n" + e);
        }
    }

