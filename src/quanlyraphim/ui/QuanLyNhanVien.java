    void clearForm() {
        NhanVien nv = new NhanVien();
        this.setForm(nv);
        this.row = -1;
        this.updateStatus();
        txtNgaySinh.setText("");
        rdoNam.setSelected(true);
        rdoNhanVien.setSelected(true);
    }

    void edit() {
        String manv = (String) tblNhanVien.getValueAt(this.row, 0);
        NhanVien nv = dao.selectById(manv);
        this.setForm(nv);
        this.updateStatus();

    }

    void setForm(NhanVien nv) {
        txtMaNV.setText(nv.getMaNV());
        txtTenNV.setText(nv.getTenNV());
        txtMatKhau.setText(nv.getMatKhau());
        txtNgaySinh.setText(XDate.toString(nv.getNgaySinh(), "dd/MM/yyyy"));
        txtSoDienThoai.setText(nv.getSoDT());
        txtDiaChi.setText(nv.getDiaChi());
        String gioiTinh = nv.getGioiTinh();
        if (gioiTinh != null && gioiTinh.equalsIgnoreCase("Nam")) {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }
//        } else if (gioiTinh != null && gioiTinh.equals("Nữ")) {
//            rdoNu.setSelected(true);
//        } 
        String vaitro = nv.getVaiTro();
        if (vaitro != null && vaitro.equalsIgnoreCase("Nhân viên")) {
            rdoNhanVien.setSelected(true);
        } else {
            rdoQuanLy.setSelected(true);
        }
//        } else if (vaitro != null && vaitro.equals("Nhân viên")) {
//            rdoNhanVien.setSelected(true);
//        }
    }

    NhanVien getForm() {
        NhanVien nv = new NhanVien();
        nv.setMaNV(txtMaNV.getText());
        nv.setTenNV(txtTenNV.getText());
        nv.setMatKhau(txtMatKhau.getText());
        nv.setNgaySinh(XDate.toDate(txtNgaySinh.getText(), "dd/MM/yyyy"));
        nv.setSoDT(txtSoDienThoai.getText());
        nv.setDiaChi(txtDiaChi.getText());
        if (rdoNam.isSelected()) {
            nv.setGioiTinh("Nam");
        } else {
            nv.setGioiTinh("Nữ");
        }
//        } else if (rdoNu.isSelected()) {
//            nv.setGioiTinh("Nữ");
//        }
        if (rdoNhanVien.isSelected()) {
            nv.setVaiTro("Nhân viên");
        } else {
            nv.setVaiTro("Quản lý");
        }
//        } else if (rdoQuanLy.isSelected()) {
//            nv.setVaiTro("Quản lý");
//        }
        return nv;
    }

