    public boolean checkValidateForm() {
        if (txtMaLoaiVe.getText().isEmpty() || txtTenLoaiVe.getText().isEmpty()) {
            MsgBox.alert(this, "Chưa nhập đủ thông tin");
            return false;
        }
        return true;
    }

    void insert() {
        LoaiVe loaive = getForm();
        try {
            if (checkValidateForm()) {
            dao.insert(loaive);
            this.fillTable();
            this.clearForm();
            MsgBox.alert(this, "Thêm thành công");
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Thêm thất bại\n" + e);
        }
    }

    void update() {
        LoaiVe loaive = getForm();
        try {
            if (checkValidateForm()) {
                dao.update(loaive);
                this.fillTable();
                MsgBox.alert(this, "Sửa thành công");
            }
            
        } catch (Exception e) {
            MsgBox.alert(this, "Sửa thất bại");
        }
    }

