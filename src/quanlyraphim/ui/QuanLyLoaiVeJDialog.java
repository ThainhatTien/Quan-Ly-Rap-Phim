    void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblLoaiVe.getModel();
        model.setRowCount(0);
        try {
            List<LoaiVe> list = dao.selectAll();
            for (LoaiVe loaive : list) {
                Object[] rows = {loaive.getMaLoaiVe(), loaive.getTenLoaiVe(),
                    loaive.getGiaVe()};
                model.addRow(rows);
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu");
        }
    }

    void edit() {
        String macd = (String) tblLoaiVe.getValueAt(this.row, 0);
        LoaiVe cd = dao.selectById(macd);
        this.setForm(cd);
//        tabs.setSelectedIndex(0);
        this.updateStatus();
    }

    void updateStatus() {
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tblLoaiVe.getRowCount() - 1);
        txtMaLoaiVe.setEditable(!edit);
        btnSave.setEnabled(!edit);
        btnUpdate.setEnabled(edit);
        btnDelete.setEnabled(edit);
//        //Directional state
//        btnPrev.setEnabled(edit && !first);
//        btnNext.setEnabled(edit && !last);
//        btnFirst.setEnabled(edit && !first);
//        btnLast.setEnabled(edit && !last);
    }

    void setForm(LoaiVe loaiVe) {
        txtMaLoaiVe.setText(loaiVe.getMaLoaiVe());
        txtTenLoaiVe.setText(loaiVe.getTenLoaiVe());
        txtGiaVe.setText(String.valueOf(loaiVe.getGiaVe()));
    }

    LoaiVe getForm() {
        LoaiVe loaive = new LoaiVe();
        loaive.setMaLoaiVe(txtMaLoaiVe.getText());
        loaive.setTenLoaiVe(txtTenLoaiVe.getText());
        loaive.setGiaVe(Float.valueOf(txtGiaVe.getText()));
        return loaive;
    }

    void clearForm() {
        LoaiVe loaive = new LoaiVe();
        this.setForm(loaive);
        this.row = -1;
        this.updateStatus();
    }
