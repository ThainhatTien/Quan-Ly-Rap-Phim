    void init() {
        setTitle("Vé");
        setLocationRelativeTo(null);
        this.fillTable();
        this.updateStatus();
        checkNhanVien();
    }

    public boolean validateForm() {
        if (txtMaVe.getText().isEmpty() || txtSoGhe.getText().isEmpty() || txtLoaiVe.getText().isEmpty()) {
            MsgBox.alert(this, "Chưa nhập đủ thông tin");
            return false;
        }
        return true;
    }

    void insert() {
        Ve ve = getForm();
        try {
            if (validateForm()) {
                dao.insert(ve);
                this.fillTable();
                this.clearForm();
                MsgBox.alert(this, "Thêm thành công");
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Thêm thất bại\n" + e);
        }
    }

    void update() {
        Ve ve = this.getForm();
        try {
            if (validateForm()) {
                dao.update(ve);
                this.fillTable();
                this.clearForm();
                MsgBox.alert(this, "Sửa thành công");
            }

        } catch (Exception e) {
            MsgBox.alert(this, "Sửa thất bại");
        }
    }

