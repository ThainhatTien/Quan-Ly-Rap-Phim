    void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblLichChieu.getModel();
        model.setRowCount(0);
        try {
            List<Ve> list = dao.selectAll();
            for (Ve ve : list) {
                Object[] row = {
                    ve.getMaVe(),
                    ve.getSoGhe(),
                    ve.getMaLoaiVe(),};
                model.addRow(row);
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu");
        }
    }

    void edit() {
        String malc = (String) tblLichChieu.getValueAt(this.row, 0);
        Ve ve = dao.selectById(malc);
        this.setForm(ve);
//        tabs.setSelectedIndex(0);
        this.updateStatus();
    }

    void updateStatus() {
        if (Auth.isManager()) {
            btnAdd.setEnabled(false);
            btnDelete.setEnabled(false);
            btnUpdate.setEnabled(false);
        } else {
            boolean edit = (this.row >= 0);
            boolean first = (this.row == 0);
            boolean last = (this.row == tblLichChieu.getRowCount() - 1);
            //form state
            txtMaVe.setEditable(!edit);
            btnAdd.setEnabled(!edit);
            btnUpdate.setEnabled(edit);
            btnDelete.setEnabled(edit);
        }
    }

    void setForm(Ve ve) {
        txtMaVe.setText(ve.getMaVe());
        txtSoGhe.setText(ve.getSoGhe());
        txtLoaiVe.setText(ve.getMaLoaiVe());
    }

    Ve getForm() {
        Ve ve = new Ve();
        ve.setMaVe(txtMaVe.getText());
        ve.setSoGhe(txtSoGhe.getText());
        ve.setMaLoaiVe(txtLoaiVe.getText());
        return ve;
    }

    void clearForm() {
        Ve ve = new Ve();
        this.setForm(ve);
        this.row = -1;
        this.updateStatus();
    }
